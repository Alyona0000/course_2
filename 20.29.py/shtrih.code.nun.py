import numpy as np

# Введення коду карти
a = input("Введіть код карти: ")

# Перевірка, чи введене значення є числом
if not a.lstrip("-").isdigit():
    print("Помилка: код має бути числом!")
    exit()

# Перетворюємо у число через numpy
d = np.int32(a)

# Перевірка, чи код є натуральним (додатним)
if d <= 0:
    print("Помилка: код має бути натуральним числом (додатним)!")
    exit()

# Перевірка діапазону допустимих кодів
if d < 102 or d > 414:
    print("Код не коректний (має бути від 102 до 414).")
    exit()

# Використовуємо numpy для обчислення масті та рангу
suit_code = d // 100
rank_code = d % 100

# Словники мастей і рангів
suits = {
    1: "Піка ♠️",
    2: "Черви ♥️",
    3: "Бубни ♦️",
    4: "Хрести ♣️"
}

ranks = {
    11: "Валет (J)",
    12: "Дама (Q)",
    13: "Король (K)",
    14: "Туз (A)"
}

# Перевірка, чи така карта існує
if suit_code not in suits or not (2 <= rank_code <= 14):
    print("Код не коректний: такої карти не існує.")
    exit()

# Формування результату
rank = ranks.get(rank_code, str(rank_code))
suit = suits[suit_code]

# Вивід результату
print(f"Ви вибрали карту: {rank} {suit}")
